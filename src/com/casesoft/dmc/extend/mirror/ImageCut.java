package com.casesoft.dmc.extend.mirror;

public class ImageCut {
    private int leftX;
    private int leftY;
    private int rightX;
    private int rightY;
    private int width;
    private int height;

    public int getLeftX() {
        return leftX;
    }
    public void setLeftX(int leftX) {
        this.leftX = leftX;
    }
    public int getLeftY() {
        return leftY;
    }
    public void setLeftY(int leftY) {
        this.leftY = leftY;
    }
    public int getRightX() {
        return rightX;
    }
    public void setRightX(int rightX) {
        this.rightX = rightX;
    }
    public int getRightY() {
        return rightY;
    }
    public void setRightY(int rightY) {
        this.rightY = rightY;
    }
    public int getWidth() {
        return width;
    }
    public void setWidth(int width) {
        this.width = width;
    }
    public int getHeight() {
        return height;
    }
    public void setHeight(int height) {
        this.height = height;
    }
}
